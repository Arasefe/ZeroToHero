package operatorsandstatements.whileloop;

public class WhileLoop1 {
    public static void main(String[] args) {
        /*
        sam age=22
        Sam is looping around the word until he reaches 30 years old.
        each year=
         */
        int ageSam=22;
        while (ageSam<=30){

            System.out.println("Sam is looping around the world "+ ageSam);
            ageSam++;
        }
    }
}
