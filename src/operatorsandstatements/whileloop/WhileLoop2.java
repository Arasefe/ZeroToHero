package operatorsandstatements.whileloop;

public class WhileLoop2 {
    public static void InfiniteLoopPractice(){
        int x=2;
        int y=5;
        while (x<10){
            y++;
            System.out.println(y);

        }
    }

    public static void main(String[] args) {
        InfiniteLoopPractice();
    }
}
