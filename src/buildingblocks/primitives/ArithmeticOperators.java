package buildingblocks.primitives;

public class ArithmeticOperators {
    public static void main(String[]args){
        int numberA=15;
        int numberB=20;
        int totalNumber=numberA+numberB;  //20+15=35
        int subtraction=numberB-numberA;  //20-15=5
        int division=numberA/5;           // 15/5=3
        int multiple=numberB*5;           //20*5+100
        int remainder=numberA%10;         //15/10=5
        System.out.println(totalNumber);
        System.out.println(subtraction);
        System.out.println(division);
        System.out.println(multiple);
        System.out.println(remainder);

    }

}
