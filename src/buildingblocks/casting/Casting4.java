package buildingblocks.casting;


public class Casting4 {
    public static void main(String[] args) {
        int x=(int)12.50;
        short y=(short)1921222;
        int z=(int)9f;
        long t=192301398193810323L;

        System.out.println("This is the int x " +x);
        System.out.println("This is short y "+ y);
        System.out.println("This is int z "+ z);
        System.out.println("This is long t " + t);
    }

}
